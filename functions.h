/**
 * @file functions.h
 * @brief Заголовочный файл с функциями.
 */

 /**
  * @brief Вычисляет сумму.
  * @param arr Массив целых чисел.
  * @return Сумма.
  */
int sumArray(const int arr[]);

/**
 * @brief Находит минимальный элемент.
 * @param arr Массив целых чисел.
 * @return Минимальный элемент.
 */
int minElement(const int arr[]);

/**
 * @brief Находит максимальный элемент.
 * @param arr Массив целых чисел.
 * @return Максимальный элемент массива.
 */
int maxElement(const int arr[]);

/**
 * @brief Определяет длину массива.
 * @param arr Массив целых чисел.
 * @return Количество элементов.
 */
int countLength(const int arr[]);

/**
 * @brief Подсчитывает количество отрицательных элементов.
 * @param arr Массив целых чисел.
 * @return Число отрицательных элементов.
 */
int countNegative(const int arr[]);
